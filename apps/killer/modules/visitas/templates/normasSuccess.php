    <h1>Las reglas del juego</h1>
    <h2>Primera y única regla</h2>

    <p>Leer la historia, para tenerlo todo claro.</p>
    
    <h2>Objetivo del juego</h2>

    <p>Los licántropos, eliminar al resto de jugadores.</p>
    <p>Los que no son licántropos, descubrir a los licántropos y eliminarlos</p>
    
    <h2>Mecánica del juego</h2>

    <p>1) CADA TARDE-NOCHE, los licántropos eligirán cada uno a un jugador a su elección y lo matarán.
      Estos jugadores pasarán a estar eliminados automáticamente.</p>
    
    <p>
      2) CADA DÍA, una vez conocidos qué jugadores han sido eliminados por los licántropos, todos los jugadores (incluídos los licántropos)
      iniciarán un debate POR CORREO en el que intentarán ponerse de acuerdo en quién es licántropo.
      Después del debate se procederá a una VOTACIÓN PÚBLICA EN ESTA WEB y la persona más votada será eliminada automáticamente.
    </p>
    
    <p>Si después de la actuación del pueblo ya no quedan licántropos en juego, se acaba el juego y ganan los jugadores que todavía no hayan
    sido eliminados.</p>
    <p>Si después de la actuación del pueblo todavía quedan licántropos en juego, estos volveran a matar volviendo al punto 1)</p>
    <p>Los licántropos ganan si el resto de jugadores son eliminados.</p>
    


    <h2>Roles</h2>
    <p><strong>Licántrop@:</strong> Nadie sabe quien es. Debe mantener su rol en secreto. Mata a un jugador cada noche. Participa en los debates y votaciones como uno más. En esta edición habrá 2 licántropos.</p>

    <p><strong>Alcalde/sa:</strong> Todo el mundo sabe quien es. Participa en los debates y votaciones como uno más. Se encarga de cerrar cada día las votaciones. En caso de empate, decide a quien hay que eliminar. Si es eliminado se nombra a otro jugador en su puesto. El Alcalde puede ser también un licántrop@.</p>

    <p><strong>Vidente:</strong> Nadie sabe quien es. Tiene la habilidad de cada día conocer el rol de uno de los jugadores. Debe mantener su rol en secreto. Participa en los debates y votaciones como uno más. Los licántrop@s no pueden tener el rol de vidente.</p>

    <p><strong>Enamorad@s</strong>: Habrá dos parejas de enamorad@s. Se conocen entre ellos pero mantienen su amor en secreto. Participan en los debates y votaciones como los demás. Si uno de los dos enamorad@s muere, el otro morirá también de pena automáticamente. Un licántrop@ también podría ser uno de los enamorad@s.</p>

 <p><strong>Bruj@:</strong> Nadie sabe quien es. Participa en los debates y votaciones como uno más. Dispone de dos pociones; una para revivir a un jugador muerto y otra para matar a uno de los jugadores. El bruj@ no puede ser licántrop@.</p>

    
<p><strong>Cazador/a:</strong> Nadie sabe quien es. Participa en los debates y votaciones como uno más. Cuando un licántropo mata a un cazador/a, el cazador puede disparar con su escopeta a un jugador a su elección y matarlo. El Cazador/a no puede ser licántrop@.</p>





