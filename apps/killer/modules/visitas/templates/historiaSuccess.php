      Revisando las grabaciones de todos los días a úĺtima hora de la tarde, se repite la escena con la misma persona sin identificar y cada
      una de los desaparecidos.
    </p>
    
    <p>
      Las grabaciones no tienen sonido, y la calidad es bastante baja, pero los expertos apuntan a que la persona sospechosa parece acosar
      y torturar a sus víctimas y después, salen juntos del edificio y se les pierde la vista. ¿Les chantajea o amenaza para que no vuelvan?
      ¿Les mata y esconde los cadáveres en algún lugar secreto? Nadie lo sabe.
    </p>
    
    <p>
      Ante la negativa de la policía a investigar la desaparición de trabajadores que van a ser despedidos igualmente en un mes, la junta directiva 
