    <h1>Las reglas del juego</h1>
    <h2>Objetivo del juego</h2>

    <p>Cada jugador tiene que matar a otro de forma "simulada" pero cuanto más parecido a la vida real mejor.</p>
    <p>Tú tienes que matar a alguien y al mismo tiempo alguien te tiene que matar a ti.</p>
    <p>Cuando te matan se acaba el juego para ti.</p>
    <p>Cuando matas, te quedas con el objetivo de tu víctima para seguir matando.</p>
    <p>El último que quede en pie, gana la partida.</p>


    <h2>¿Cómo participo?</h2>
    <p>Para participar en el killer edición 2012, debes registrarte en la web.</p>
    <p>Los datos de <strong>Nombre</strong> y <strong>Departamento</strong> deben ser reales. Si tu departamento no aparece en la lista, pon otro cualquiera y avísanos para poder cambiarlo antes de que comience el juego.</p>
    <p>Los datos de <strong>Alias</strong>, <strong>Biografía</strong> y <strong>Foto</strong> son inventados. Utiliza tu imaginación.</p>
    <p>Los datos de <strong>Usuario</strong> y <strong>Contraseña</strong> son para entrar en la parte privada de la web.</p>
    <p>El <strong>email</strong> es por si tenemos que escribiros por algún motivo urgente. No obstante se intentará que toda la información necesaria aparezca en la web.</p>



    <h2>Inicio del juego</h2>

    <p>Unas horas antes del inicio del juego, cada jugador será informado a través de la web de cuál es su primera víctima.</p>
    <p>El objetivo de cada jugador es ir eliminando a cada una de sus víctimas hasta ser el último jugador que quede vivo.</p>



    <h2>Eliminación</h2>

    <p>Para eliminar una víctima hay que tener un arma preparada (pistola de papel o plástico, cuchillo de papel o plástico, ... de papel o plástico). No está permitido hacer daño a nadie (por si acaso alguien no lo intuía. :D)</p>
    <p>La Eliminación debe simular una situación que se pueda dar en la vida "real". Por ejemplo, matar a alguien enviando un email todavía no se ha inventado, mándale una carta-bomba, que eso sí que existe.</p>
    <p>No vale matar con las manos desnudas (Ve siempre con un arma preparada por si encuentras a tu víctima de forma inesperada).</p>
    <p>NADIE excepto tu víctima, DEBE PODER IDENTIFICARTE como autor del asesinato. Debes tener en cuenta que si algún testigo te identifica como autor del crimen, irás directamente a la cárcel con condena de cadena perpétua. </p>
    <p>Siempre ha de haber alguien que pueda CERTIFICAR LA MUERTE de tu objetivo, para que la víctima no pueda alegar que se libró. Dicha persona puedes ser tú mismo o un jugador cómplice en el que confíes plenamente y sepas que no te va a denunciar ni identificar como asesino. Por ejemplo, si pones una "maceta" de cartón encima de la puerta del despacho de tu víctima pero nadie presencia la muerte, tu víctima podrá decir que vio la puerta entreabierta, se fijó en la maceta y abrió la puerta sin entrar para que la maceta cayera al suelo sin que le cayera encima. Si no estás tú o cualquier otra persona de testigo tu víctima siempre podrá negar su muerte</p>
    <p>No es obligatorio cambiar de arma con cada objetivo, pero un buen asesino en serie no debe repetir arma para que no se relacionen sus crímenes.</p>
    <p>No puedes matar a alguien que no sea tu objetivo.</p>




    <h2>Después de la Eliminación</h2>
    <p>Después de eliminar a tu víctima entra en la web e indica que has eliminado a tu objetivo escribiendo un pequeño relato de la muerte que se publicará en el blog.</p>
    <p>Cuando tu víctima confirme su propia muerte se te asignará un nuevo objetivo.</p>
    <p>El Jugador eliminado debe confirmar su muerte en la web y dejará automáticamente de jugar.</p>

    <h2>Otras normas</h2>

    <p>El "terreno de juego" está limitado al recinto del ITE (de la barrera hacia dentro).</p>
    <p>Se ruega la máxima discreción y buena fe en todo momento.</p>

